#include "libft.h"

char	*ft_strdup(char *str)
{
	int		i;
	char	*p;

	p = (char *)malloc((ft_strlen(str) + 1) * sizeof(char));
	if (!p)
		return (0);
	i = 0;
	while (str[i])
	{
		p[i] = str[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
